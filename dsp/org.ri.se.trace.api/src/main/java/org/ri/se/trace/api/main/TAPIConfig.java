package org.ri.se.trace.api.main;

public class TAPIConfig {

	private String urlEther;
	private String urlIPFS;
	private String walletDir;
	private String traceContract;
	private String urlWeb;
	private String urlCentraliRegistration;
	private String resourceAPI;
	
	
	public String getUrlEther() {
		return urlEther;
	}
	public void setUrlEther(String urlEther) {
		this.urlEther = urlEther;
	}
	public String getUrlIPFS() {
		return urlIPFS;
	}
	public void setUrlIPFS(String urlIPFS) {
		this.urlIPFS = urlIPFS;
	}
	public String getWalletDir() {
		return walletDir;
	}
	public void setWalletDir(String walletDir) {
		this.walletDir = walletDir;
	}
	public String getTraceContract() {
		return traceContract;
	}
	public void setTraceContract(String traceContract) {
		this.traceContract = traceContract;
	}
	public String getUrlWeb() {
		return urlWeb;
	}
	public void setUrlWeb(String urlWeb) {
		this.urlWeb = urlWeb;
	}
	public String getUrlCentraliRegistration() {
		return urlCentraliRegistration;
	}
	public void setUrlCentraliRegistration(String urlCentraliRegistration) {
		this.urlCentraliRegistration = urlCentraliRegistration;
	}
	public String getResourceAPI() {
		return resourceAPI;
	}
	public void setResourceAPI(String resourceAPI) {
		this.resourceAPI = resourceAPI;
	}
	
	
	
}
