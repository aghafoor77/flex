package com.rise.vdr.api.utils;

import java.util.Map;

public class AccessLevelEntity {

	private Map<String, Object> animals;
	private String transporter;
	private String slaughterhouse;
	private String from;
	
	public Map<String, Object> getAnimals() {
		return animals;
	}
	public void setAnimals(Map<String, Object> animals) {
		this.animals = animals;
	}
	public String getTransporter() {
		return transporter;
	}
	public void setTransporter(String transporter) {
		this.transporter = transporter;
	}
	public String getSlaughterhouse() {
		return slaughterhouse;
	}
	public void setSlaughterhouse(String slaughterhouse) {
		this.slaughterhouse = slaughterhouse;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	
	
	
	
}
